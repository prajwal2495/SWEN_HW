public class Caroll
{
    /*
    Usage of escape characters and line feed in System.out
     */
    public static void main(String args[])
    {
        System.out.print("\"Begin at the\n"); // escape character for double quotes
        System.out.print("beginning,\", the King\n");
        System.out.print("said, very gravely, \"and\n"); // line feed
        System.out.print("go on till you come to\n");
        System.out.print("the end: then stop.\n");
        System.out.print("            -Lewis Caroll\n");
    }
}
